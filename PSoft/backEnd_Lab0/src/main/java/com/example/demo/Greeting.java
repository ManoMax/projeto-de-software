package com.example.demo;

public class Greeting {

	private String name;
	private String greeting;
	
	public Greeting(String name, String greeting) {
		this.name = name;
		this.greeting = greeting;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String getGreeting() {
		return this.greeting;
	}
	
}
